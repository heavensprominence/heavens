export function getCurrencyWithCRED(currencyCode: string) {
  const currency = WORLD_CURRENCIES.find((c) => c.code === currencyCode)
  if (!currency) return null

  return {
    ...currency,
    credCode: `${currencyCode}-CRED`,
    credName: `${currency.name} CRED`,
  }
}

export async function getExchangeRates() {
  try {
    // Using a free exchange rate API
    const response = await fetch("https://api.exchangerate-api.com/v4/latest/USD")
    const data = await response.json()
    return data.rates
  } catch (error) {
    console.error("Failed to fetch exchange rates:", error)
    // Return default rates if API fails
    return { USD: 1, EUR: 0.85, GBP: 0.73, CAD: 1.25, JPY: 110 }
  }
}
